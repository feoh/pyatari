"""Generate BENCHMARKS.md from a pytest-benchmark JSON result file.

Usage:
    uv run python benchmarks/report.py benchmarks/results/baseline.json

Produces BENCHMARKS.md at the project root.
"""

from __future__ import annotations

import json
import sys
from datetime import datetime, timezone
from pathlib import Path


_MODULE_LABELS: dict[str, str] = {
    "bench_cpu": "CPU",
    "bench_memory": "Memory Bus",
    "bench_rendering": "Rendering (ANTIC/GTIA)",
    "bench_machine": "Full Machine / Frame",
    "bench_input": "Input Handling",
}

_FPS_BENCHMARKS = {
    "bench_machine_run_frame",
    "bench_machine_run_frame_with_audio",
    "bench_machine_run_frame_demo",
}


def _format_time(seconds: float) -> str:
    if seconds < 1e-6:
        return f"{seconds * 1e9:.1f} ns"
    if seconds < 1e-3:
        return f"{seconds * 1e6:.2f} µs"
    if seconds < 1.0:
        return f"{seconds * 1e3:.2f} ms"
    return f"{seconds:.3f} s"


def _module_of(bm: dict) -> str:
    # Use the fullname (file path) to determine module, falling back to function name
    fullname = bm.get("fullname", "")
    for prefix, label in _MODULE_LABELS.items():
        if f"/{prefix}.py" in fullname or f"\\{prefix}.py" in fullname:
            return label
    # Fallback: strip "test_" prefix and match function name
    stripped = bm.get("name", "").removeprefix("test_")
    for prefix, label in _MODULE_LABELS.items():
        if stripped.startswith(prefix):
            return label
    return "Other"


def _fps_annotation(name: str, mean_s: float) -> str:
    stripped = name.removeprefix("test_")
    if any(stripped.startswith(bm) for bm in _FPS_BENCHMARKS):
        fps = 1.0 / mean_s if mean_s > 0 else 0
        return f" ({fps:.1f} fps)"
    return ""


def generate(json_path: Path, output_path: Path) -> None:
    data = json.loads(json_path.read_text())

    benchmarks: list[dict] = data.get("benchmarks", [])
    if not benchmarks:
        print(f"No benchmarks found in {json_path}", file=sys.stderr)
        sys.exit(1)

    machine_info = data.get("machine_info", {})
    commit_info = data.get("commit_info", {})
    run_at = data.get("datetime", "unknown")

    # Group by module
    groups: dict[str, list[dict]] = {}
    for bm in benchmarks:
        module = _module_of(bm)
        groups.setdefault(module, []).append(bm)

    lines: list[str] = []
    lines.append("# PyAtari Performance Benchmarks")
    lines.append("")
    lines.append(
        "This file is auto-generated by `benchmarks/report.py`. "
        "Run benchmarks and regenerate with:"
    )
    lines.append("```bash")
    lines.append("uv run pytest benchmarks/ --benchmark-save=<label>")
    lines.append("uv run python benchmarks/report.py benchmarks/results/<label>.json")
    lines.append("```")
    lines.append("")

    # Metadata block
    lines.append("## Run Info")
    lines.append("")
    lines.append(f"| Field | Value |")
    lines.append(f"|-------|-------|")
    lines.append(f"| Date | {run_at} |")
    if machine_info:
        cpu = machine_info.get("cpu", {})
        lines.append(f"| CPU | {cpu.get('brand_raw', 'unknown')} |")
        lines.append(f"| Python | {machine_info.get('python_version', 'unknown')} |")
    if commit_info:
        rev = commit_info.get("id", "")[:8]
        branch = commit_info.get("branch", "")
        lines.append(f"| Git | {branch} @ {rev} |")
    lines.append("")

    # Benchmark tables per module
    for module_label in _MODULE_LABELS.values():
        bms = groups.get(module_label, [])
        if not bms:
            continue

        lines.append(f"## {module_label}")
        lines.append("")
        lines.append("| Benchmark | Mean | Std Dev | Min | Max | Rounds |")
        lines.append("|-----------|------|---------|-----|-----|--------|")

        for bm in sorted(bms, key=lambda b: b["stats"]["mean"]):
            name = bm["name"]
            stats = bm["stats"]
            mean = stats["mean"]
            stddev = stats["stddev"]
            min_ = stats["min"]
            max_ = stats["max"]
            rounds = stats["rounds"]
            fps = _fps_annotation(name, mean)
            lines.append(
                f"| `{name}` | {_format_time(mean)}{fps} | "
                f"{_format_time(stddev)} | {_format_time(min_)} | "
                f"{_format_time(max_)} | {rounds} |"
            )
        lines.append("")

    # Key numbers summary
    frame_bm = next(
        (b for b in benchmarks if b["name"] in {"test_bench_machine_run_frame", "bench_machine_run_frame"}), None
    )
    frame_demo_bm = next(
        (b for b in benchmarks if b["name"] in {"test_bench_machine_run_frame_demo", "bench_machine_run_frame_demo"}), None
    )

    if frame_bm or frame_demo_bm:
        lines.append("## Key Numbers")
        lines.append("")
        if frame_bm:
            mean = frame_bm["stats"]["mean"]
            fps = 1.0 / mean if mean > 0 else 0
            lines.append(
                f"- **Frame throughput (NOP sled):** {_format_time(mean)}/frame "
                f"= **{fps:.1f} fps** (real-time target: 60 fps)"
            )
        if frame_demo_bm:
            mean = frame_demo_bm["stats"]["mean"]
            fps = 1.0 / mean if mean > 0 else 0
            lines.append(
                f"- **Frame throughput (demo screen):** {_format_time(mean)}/frame "
                f"= **{fps:.1f} fps** (real-time target: 60 fps)"
            )
        lines.append("")

    output_path.write_text("\n".join(lines) + "\n")
    print(f"Written: {output_path}")


def main() -> None:
    if len(sys.argv) != 2:
        print(f"Usage: {sys.argv[0]} <benchmark-json-file>", file=sys.stderr)
        sys.exit(1)

    json_path = Path(sys.argv[1])
    if not json_path.exists():
        print(f"File not found: {json_path}", file=sys.stderr)
        sys.exit(1)

    # Output always goes to BENCHMARKS.md at project root
    output_path = Path(__file__).parent.parent / "BENCHMARKS.md"
    generate(json_path, output_path)


if __name__ == "__main__":
    main()
